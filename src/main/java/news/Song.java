package news;

public class Song {
    private String title;
    public String artist;

    // getter method
    public String getTitle(){
        return this.title;
    }
    //setter method
    public void setTitle(String title){
        this.title = title;
    }
    public String getArtist(){
        return this.artist;
    }
    public void setArtist(String artist){
        this.artist = artist;
    }
    }


